int		is_flag(char c)
{
	return (c == '+' || c == '-' || c == ' ' || c == '#' || c == '0');
}

int		is_num(char c)
{
	return (c > '0' && c <= '9');
}

int		is_mod(char c)
{
	return (c == 'h' || c == 'l' || c == 'j' || c == 'z');
}
